const numbers = [1, 2, 3, 4, 5]; // Um ArrayList de números
const output = numbers.map((x) => { // Declaração de variável que armazenar o retorno da função map() do ArrayList numbers, que irá iterar sobre cada elemento e retorna a multiplicaçaõ por 2
  return x * 2;
});
console.log(output); // Imprimir na saída do console(terminal) o output

