const arr = [1, 2, 3, 4, 5]; // Um ArrayList de números
const output = arr.filter((x) => x > 4); // Variável que guarda o ArrayList retornado da ação feita pela função filter que retorna somente os elementos que são maior que 4
console.log(output); // Mostra na saída(terminal) a variável output

